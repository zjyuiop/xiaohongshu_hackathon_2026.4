function buildBundledAgents(personId: string, displayName: string, nodes: TimelineNode[]): PersonaSpec[] {
  return nodes.map((node) => ({
    agentId: `${node.nodeId}-agent`,
    displayName: `${displayName} · ${node.stageLabel}`,
    personId,
    avatarSeed: `${personId}-${node.stageType}`,
    timeLabel: node.timeLabel,
    stageLabel: node.stageLabel,
    keyEvent: node.keyEvent,
    knownFacts: [node.summary, ...node.tensions],
    sourceEvidence: node.sourceEvidence,
    traits: node.traits,
    values: node.values,
    goal: '守住此阶段最在意的东西',
    fear: '害怕代价失控',
    voiceStyle: '表达直接但带阶段局限',
    knowledgeBoundary: `只能知道 ${node.timeLabel} 及之前已经发生的事实。`,
    forbiddenFutureKnowledge: true,
    stanceSeed: node.stageType === 'crisis' ? '倾向保守，先降低损失' : '倾向行动，但重视代价',
  }));
}

export function getBundledDefaultProfiles(): BundledDefaultProfile[] {
  return bundledPresetProfiles.map((profile) => {
    const nodes = bundledTimelineMap[profile.id] ?? [];
    return {
      profile,
      nodes,
      agents: buildBundledAgents(profile.id, profile.displayName, nodes),
    };
  });
}
